    private void existente(){
        String idInput = ID_modif.getText().trim(); // Obtener el texto y quitar espacios

    if (idInput.isEmpty()) {
        return; // Si está vacío, no hacer nada
    }
    Connection con = null;
        PreparedStatement pstUsuario = null;
        ResultSet rsUsuario = null;
        try {
            con = DatabaseConnection.getConnection(); // Asegúrate de que este método maneje excepciones
        // Verificar si el DNI ya existe
        String queryDNI = "SELECT *  FROM Empleado WHERE idempleado = ?";
        pstUsuario = con.prepareStatement(queryDNI);
        pstUsuario.setString(1, idInput);
        rsUsuario = pstUsuario.executeQuery();

        if (rsUsuario.next() && rsUsuario.getInt(1) > 0) {
           Modifica newframe = new Modifica();
        
            newframe.setVisible(true);
        
            this.dispose();
             // Salir del método si el DNI ya existe


        } else {
            // Si el conteo es 0, el ID no existe
            JOptionPane.showMessageDialog(null, "El ID ingresado no existe. Vuelve a intentar", 
                                          "Información", JOptionPane.WARNING_MESSAGE);
        }
    } catch (Exception e) {
        e.printStackTrace(); // Manejo de errores
    } finally {
        // Cerrar conexiones
        try {
            if (rsUsuario != null) rsUsuario.close();
            if (pstUsuario != null) pstUsuario.close();
            if (con != null) con.close();
        } catch (Exception e) {
            e.printStackTrace(); // Manejo de errores al cerrar
        }
    }

    }
