private boolean validarEmail(String email) {
        return email != null && email.contains("@");
    }

    private void btnregistroActionPerformed(java.awt.event.ActionEvent evt) {
    
    String usuario = txtusuario.getText();
    String contrasena = new String(passwordcontraseña.getPassword());
    String direccion = txtdireccion.getText();  // Obtener el valor de la dirección
    String email = txtemail.getText();          // Obtener el valor del email

    // Verificar que ningún campo esté vacío
    if (usuario.isEmpty() || contrasena.isEmpty() || direccion.isEmpty() || email.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios. Por favor, complétalos.", "Advertencia", JOptionPane.WARNING_MESSAGE);
        return;
    }
    // Verificar arroba
    if (!validarEmail(email)) {
            JOptionPane.showMessageDialog(this, "El correo electrónico debe contener una arroba (@).", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

    // Verificar si los datos del usuario ya existen
    if (DatabaseConnection.usuarioCompletoExiste(usuario, email, IDUsuario)) {
        JOptionPane.showMessageDialog(this, "Ya existe un usuario con esos datos. Por favor, elige otros.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    try {
        // Registrar el nuevo usuario y obtener su ID
        int IDUsuario = DatabaseConnection.registrarUsuario(usuario, contrasena, direccion, email);
        
        // Asignar membresía gratuita al nuevo usuario registrado
        DatabaseConnection.asignarMembresiaGratuita(IDUsuario);
        
        JOptionPane.showMessageDialog(this, "Registro exitoso. Se ha asignado membresía gratuita.", "Éxito", JOptionPane.INFORMATION_MESSAGE);

        // Limpiar campos
        txtusuario.setText("");
        passwordcontraseña.setText("");
        txtdireccion.setText("");
        txtemail.setText("");

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error al registrar el usuario: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
}

